package com.javaLogicBuilding;

public class Example1 {

	public static void main(String[] args) {
		
		//print vertical straight line using star pattern
		for(int i = 1; i <= 10; i++)
		{
			System.out.println("*");
		}
		
		System.out.println("-------------------------------------");
		
		//print horizontal straight line using star pattern
		for(int i = 1; i <= 10; i++)
		{
			System.out.print("* ");
		}

	}

}
